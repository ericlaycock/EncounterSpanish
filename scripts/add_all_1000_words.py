def add_all_1000_words():
    """Add/update all 1000 high-frequency words"""
    db: Session = SessionLocal()
    try:
        added_count = 0
        updated_count = 0
        skipped_count = 0
        seen_words = set()
        
        for rank, spanish, english in ALL_1000_WORDS:
            # Skip excluded standalone grammar words
            if spanish in EXCLUDED_STANDALONE:
                skipped_count += 1
                continue
                
            # Skip duplicates within the list
            if spanish in seen_words:
                skipped_count += 1
                continue
            seen_words.add(spanish)
            
            word_id = f"hf_{spanish}"
            existing_word = db.query(Word).filter(Word.id == word_id).first()
            
            if existing_word:
                existing_word.word_category = 'high_frequency'
                existing_word.frequency_rank = rank
                if not existing_word.english or existing_word.english == "":
                    existing_word.english = english
                updated_count += 1
            else:
                new_word = Word(
                    id=word_id,
                    spanish=spanish,
                    english=english,
                    word_category='high_frequency',
                    frequency_rank=rank
                )
                db.add(new_word)
                added_count += 1
            
            if (added_count + updated_count) % 50 == 0:
                db.commit()
        
        db.commit()
        print(f"‚úÖ Added {added_count} high-frequency words")
        print(f"‚úÖ Updated {updated_count} existing words")
        print(f"‚ö†Ô∏è  Skipped {skipped_count} words (excluded grammar words or duplicates)")
        print(f"üìä Total processed: {added_count + updated_count} words")
        
    except Exception as e:
        db.rollback()
        print(f"‚ùå Error: {e}")
        import traceback
        traceback.print_exc()
        raise
